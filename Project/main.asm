.MACRO KEYBOARD_CHECK_COL			; Row x is LOW, check columns
	NOP
	SBIS PIND,3						; Skip next instruction if not pressed
	RJMP @0
	SBIS PIND,2
	RJMP @1
	SBIS PIND,1
	RJMP @2
	SBIS PIND,0
	RJMP @3
	RJMP @4
.ENDMACRO

.MACRO FIND_SCORES					; @0 score  @1  ten   @2 unit
	LDI R26,0x64
	CP @0,R26					
	BRGE except						;  Branch if current score >= 100
	LDI @1,0x00
	LDI R18,0x0A
	LDI R27,0x0A
	decaade:
		CP @0,R18
		BRLO numbers
		ADD R18,R27
		INC @1
		BRNE decaade
	numbers:
		SUB R18,R27
		SUB @0,R18
		LDI @2,0x00
		loopnb:
			CPSE @0,@2
			RJMP notthis
			RJMP end_mac
		notthis:
			INC @2
			BRNE loopnb
	except:
		LDI @1,0x09
		LDI @2,0x09
		RJMP end_mac
	end_mac:
.ENDMACRO

.MACRO DELAY_MACRO
	CALL Buzzer_loop	
	LDI R26,0x01 
	LDI R27,0x02
	LDI R28,0x80
	loop13:
		loop23:
			loop33:
				LDI ZH,high(@1<<1)
				LDI ZL,low(@1<<1)
				CALL SCREEN_PRINT_LOOP
				SBIC PINB,2
				RJMP NoJoy
				RJMP @0
				NoJoy:
				DEC R28
				BRNE loop33
			DEC R27
			BRNE loop23
		DEC R26
		BRNE loop13
.ENDMACRO


