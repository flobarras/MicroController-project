	Joy_start:
		CLI							; Stop timer (Global Interrupt Disable)
		LDS R22,TCNT1L				; Seed for generation of random numbers 
		SEI							; (random as we use the time at which the user presses the joystick)
		LDI R26,0x01
		CPSE R28,R26
		MOV R22,R18
		LDI R25,0x00
		STS TIMSK0,R25
		MOV R29,R22
		LDI R30,0x00				; Counter for the sequence
		LDI R31,0x01				; Length of the sequence
		LDI R19,0x00				; For speed
		LDI R21,0x00				; Score
		RJMP main


/*************************\
|* MAIN LOOP OF THE GAME *|
\*************************/

main: 

	MOV R30,R31			; Counter for the sequence is equal to its length (increased every time the player succeeds)

	; PART 1 : The screen is divided in four parts, that lit up randomly (only one part is lit up at one time)		
	CALL SEE_SEQ

	; PART 2 : The user must reproduce the sequence. The game ends when the user makes an error, or when he does not do any action
	; during 15	consecutive seconds
	CALL REDO_SEQ


/*******************\
|* END OF THE GAME *|
\*******************/

end:
	LDI R25,0x00
	STS TIMSK0,R25				; Disable interrpt for timer 0 (to stop error sound)
	CBI PORTB,1

	CLI							; Disable all interrupts (to not interfer with reading operation of the EEPROM)
	LDI R18,0x00
	LDI R17,0x00
	CALL EEPROM_READ			; Load the stored highest score	(in R30) from the EEPROM (non-volatile memory)
	SEI
					
	CP R21,R30					; Compare stored high score to current score (R21)
	BRGE newHighScore			;  Branch if R21 >= R30 (unsigned)
	RJMP end_s

	newHighScore:
		MOV R30,R21
		CLI
		LDI R18,0x00
		LDI R17,0x00
		CALL EEPROM_WRITE		; Write in EEPROM the new best score
		SEI
		


end_s:
	FIND_SCORES R21,R29,R22		; Find out the ten and unit of current score, in order to print it
	FIND_SCORES R30,R19,R20		; Find out the ten and unit of best score, in order to print it
	CLI							; Stop timer (Global Interrupt Disable)
	LDS R18,TCNT1L				; Seed for generation of random numbers for the next game 
	SEI							; Global enable interrupt
	LDI R25,0x00
	STS TIMSK1,R25				; Disable interrupt for timer 1


/******************************************************************************\
|* END SCREEN : SCORE + BEST SCORE + PRESS JOYSTICK TO START THE GAME + MUSIC *|
\******************************************************************************/
			
end_screen:

	; Alternatively print the score, the best score and the text "Press JS to start"
	; Plus: melody
	LDI R24,0x01
	LDI R23,0xA5							; Key F (4)	 	
	CALL Delay1
	LDI R23,0xB8							; Key A#			 	
	CALL Delay1
	LDI R23,0x8E							; Key C#		 
	CALL Delay1
	LDI R24,0x00
	LDI R23,0xA5							; Key F		 	
	CALL Delay1
	LDI R23,0x88							; Key C			 
	CALL Delay1
	LDI R23,0x9B							; Key D#			 
	CALL Delay1
	LDI R23,0xAF							; Key G			 	
	DELAY_MACRO Joy,Press_JS_to_start
	LDI R23,0xAB							; Key F#		 		
	DELAY_MACRO Joy,Press_JS_to_start

	RJMP end_screen

	
Joy:
	LDI R25,0x00
	STS TIMSK0,R25			; Disable interrupt for timer 0
	RJMP init




/*****************************\
|* TIMER INTERRUPT'S ACTIONS *|
\*****************************/

Timer0OverflowInterrupt:
	; Used for the buzzer
	; If switch is low : no sound
	OUT TCNT0,R23				; TCNTinit
	SBIC PINB,0					; Skip next instruction if bit 0 of pinB is cleared
	RJMP switchHigh				
	switchLow:
		LDI R23,0x00
		STS TIMSK0,R23
		RETI
	switchHigh:
		SBI PINB,1					; invert output of buzzer
		RETI
	

Timer1OverflowInterrupt:
	; Used to increase the speed during the game, and to check for user inactivity
	; Plus, at the beginning, to get a random seed
	PUSH R25
	PUSH R23
	LDI R23,0x85                ; TCNTinit (2 Hz) 
	LDI R25,0xED					
	STS TCNT1H,R23					
	STS TCNT1L,R25 
	
	LDI R25,0xFF
	CPSE R18,R25
	RJMP wait_state
	RJMP button_pressed

	button_pressed:
		LDI R24,0x00
		RJMP continue

	wait_state:
		INC R24
		RJMP continue

	continue:
	LDI R25,0x40
	CPSE R19,R25
	RJMP inc_speed
	RJMP max_speed
	inc_speed:
		INC R19
		POP R23
		POP R25
		RETI
	max_speed:
		POP R23
		POP R25
		RETI



